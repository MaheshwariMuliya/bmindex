                    buildInstructionStep('3. Keeping your right leg straight out in front of you off the floor, raise your left leg slowly up to 90 degrees.'),
                    buildInstructionStep('4. Slowly lower your left leg back to the starting position without touching the floor and repeat the movement with your right leg.'),
